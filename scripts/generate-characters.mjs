// Generates the 33 type characters + per-type CSS palettes + a review
// contact sheet. Deterministic; outputs are committed.
//
//   assets/img/characters/{id}.svg   (consumers reference by id only, so a
//                                     raster replacement just drops {id}.png
//                                     beside it and flips the template ext)
//   assets/css/type-palettes.css     (.t-{id} { --type-primary: ... })
//   scripts/preview-characters.html  (local review contact sheet)
//
// Usage: node scripts/generate-characters.mjs

import { writeFileSync, mkdirSync } from 'node:fs';
import { join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { TYPES, palette } from './characters/type-meta.mjs';
import { buildCharacterSvg } from './characters/render-character.mjs';

const ROOT = join(fileURLToPath(import.meta.url), '..', '..');
const CHAR_DIR = join(ROOT, 'assets', 'img', 'characters');
mkdirSync(CHAR_DIR, { recursive: true });

// --- character SVGs ----------------------------------------------------------
for (const meta of TYPES) {
  writeFileSync(join(CHAR_DIR, `${meta.id}.svg`), buildCharacterSvg(meta));
}
console.log(`wrote ${TYPES.length} character SVGs to assets/img/characters/`);

// --- per-type CSS custom properties -------------------------------------------
let css = `/* GENERATED by scripts/generate-characters.mjs — do not edit by hand.
   Apply .t-{type-id} on a container to theme pc- components for that type. */
`;
for (const meta of TYPES) {
  const c = palette(meta.hue);
  css += `.t-${meta.id} { --type-primary: ${c.primary}; --type-secondary: ${c.secondary}; --type-soft: ${c.soft}; }\n`;
}
writeFileSync(join(ROOT, 'assets', 'css', 'type-palettes.css'), css);
console.log('wrote assets/css/type-palettes.css');

// --- contact sheet for review --------------------------------------------------
const cells = TYPES.map(
  (m) => `<figure><img src="../assets/img/characters/${m.id}.svg" width="160" height="160" alt="${m.id}"><figcaption><b>${m.id}</b><br>${m.name.en}<br>${m.name.ja}</figcaption></figure>`,
).join('\n');
writeFileSync(
  join(ROOT, 'scripts', 'preview-characters.html'),
  `<!doctype html><meta charset="utf-8"><title>Character contact sheet</title>
<style>body{font-family:sans-serif;background:#fafafa;margin:24px}
main{display:grid;grid-template-columns:repeat(auto-fill,minmax(180px,1fr));gap:16px}
figure{margin:0;padding:12px;background:#fff;border-radius:12px;box-shadow:0 2px 8px rgba(0,0,0,.08);text-align:center}
figcaption{font-size:12px;color:#555;margin-top:8px;line-height:1.5}</style>
<h1>Profilecode characters (${TYPES.length})</h1><main>${cells}</main>
`,
);
console.log('wrote scripts/preview-characters.html');
